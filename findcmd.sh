findcmd() {
  set -- "${2+"$1"}" "${2-"$1"}" "$PATH:" ":"
  PATH=: && set -- "$1" "$2" "${3%:}${PATH}" "$PATH" && PATH=${3%:}
  while [ "$3" ]; do
    set -- "$1" "$2" "${3#*"$4"}" "$4" "${3%%"$4"*}"
    set -- "$1" "$2" "$3" "$4" "${5:-.}/$2"
    [ -e "$5" ] && [ -f "$5" ] && eval "${1:-: }=\$5" && return 0
  done
  return 1
}
